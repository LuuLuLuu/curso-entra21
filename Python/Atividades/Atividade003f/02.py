"""
2. Escreva um programa que calcule a soma de 3 números e exiba o resultado. 
Os 3 números devem ser informados pelo usuário no momento da execução.
"""

num1 = int(input("Digite um número: "))
num2 = int(input("Digite outro número: "))
num3 = int(input("Digite outro número: "))
soma = num1 + num2 + num3
print(f"A soma dos três números informados é de: {soma}")
